package nhomKpop;

public class nhom {
private String ten;
private String soluong;

public nhom(String ten, String soluong) {
	super();
	this.ten = ten;
	this.soluong = soluong;
}
public String getTen() {
	return ten;
}
public void setTen(String ten) {
	this.ten = ten;
}
public String getSoluong() {
	return soluong;
}
public void setSoluong(String soluong) {
	this.soluong = soluong;
}

}
